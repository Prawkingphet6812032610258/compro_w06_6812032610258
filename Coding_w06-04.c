#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับการแสดงผล (printf)

int main() {
    // ----------------------------- ตัวอย่างที่ 1 -----------------------------
    // แสดงการลบค่าออกจากตัวแปร
    double x1 = 10.0, x2 = 10.0; // กำหนดค่าเริ่มต้นของ x1 และ x2 เป็น 10.0
    x1 = x1 - 4.0; // แบบเต็ม: นำค่า x1 (10.0) มาลบด้วย 4.0 ได้ผลลัพธ์เป็น 6.0
    x2 -= 4.0; // แบบย่อ: เป็นการลดค่า x2 ลงด้วย 4.0 (x2 = x2 - 4.0)
    printf("1) x1 = %.2f, x2 = %.2f\n", x1, x2); // แสดงผลลัพธ์ x1 และ x2 โดยแสดงทศนิยม 2 ตำแหน่ง

    // ----------------------------- ตัวอย่างที่ 2 -----------------------------
    // แสดงการคูณค่าคงที่เข้ากับตัวแปร
    x1 = 2.0; x2 = 2.0; // เริ่มต้นใหม่โดยกำหนด x1 และ x2 เป็น 2.0
    x1 = 6.5 * x1; // แบบเต็ม: คูณค่า 6.5 เข้ากับ x1 (2.0) ได้ 13.0
    x2 *= 6.5; // แบบย่อ: เป็นการคูณค่า x2 ด้วย 6.5 (x2 = x2 * 6.5)
    printf("2) x1 = %.2f, x2 = %.2f\n", x1, x2);

    // ----------------------------- ตัวอย่างที่ 3 -----------------------------
    // การใช้ตัวดำเนินการหารเอาเศษ (%)
    int x3 = 35, x4 = 35; // ตัวแปรชนิดจำนวนเต็มเพื่อใช้กับ % ได้
    int y = 3, z = 2, a = 4; // กำหนดค่าตัวแปรเพิ่มเติม
    x3 = x3 % (y + z * a); // แบบเต็ม: คำนวณด้านขวาก่อน (y + z * a) = 3 + 2*4 = 11 -> x3 % 11 = 35 % 11 = 2
    x4 %= (y + z * a); // แบบย่อ: ทำแบบเดียวกัน (x4 = x4 % (y + z * a))
    printf("3) x3 = %d, x4 = %d\n", x3, x4);

    // ----------------------------- ตัวอย่างที่ 4 -----------------------------
    // การหารค่าของตัวแปรด้วยนิพจน์ที่มีตัวแปรเอง
    x1 = 4.0; x2 = 4.0; // เริ่มต้นที่ 4.0
    x1 = x1 / (2.0 * x1); // แบบเต็ม: 4.0 / (2.0 * 4.0) = 4.0 / 8.0 = 0.5
    x2 /= (2.0 * x2); // แบบย่อ: ทำแบบเดียวกัน (x2 = x2 / (2.0 * x2))
    printf("4) x1 = %.2f, x2 = %.2f\n", x1, x2);

    // ----------------------------- ตัวอย่างที่ 5 -----------------------------
    // การคำนวณมูลค่ารวม โดยมีการลดราคาก่อน
    float total1 = 100.0, total2 = 100.0; // เริ่มต้นที่มูลค่ารวม 100.0
    float price = 20.0, quantity = 3.0, discount = 10.0; // ราคาต่อชิ้น, จำนวน, ส่วนลด
    total1 = total1 + (price * quantity - discount); 
    // แบบเต็ม: คำนวณมูลค่าก่อนลด = 20.0 * 3.0 = 60.0, หักส่วนลด 10.0 เหลือ 50.0, บวกกับ total1 = 150.0
    total2 += (price * quantity - discount); 
    // แบบย่อ: เพิ่มค่า (ราคา * จำนวน - ส่วนลด) เข้าไปใน total2
    printf("5) total1 = %.2f, total2 = %.2f\n", total1, total2);

    // ----------------------------- ตัวอย่างที่ 6 -----------------------------
    // การคูณด้วย (1 + อัตราร้อยละ) เช่น กรณีคิดดอกเบี้ย
    x1 = 100.0; x2 = 100.0; // เริ่มต้นที่ 100.0
    double rate = 5.0; // อัตราร้อยละ 5%
    x1 = x1 * (1 + rate / 100); 
    // แบบเต็ม: 100.0 * (1 + 5.0/100) = 100.0 * 1.05 = 105.0
    x2 *= (1 + rate / 100); 
    // แบบย่อ: คูณตัวเองด้วย 1.05
    printf("6) x1 = %.2f, x2 = %.2f\n", x1, x2);

    // ----------------------------- ตัวอย่างที่ 7 -----------------------------
    // การหักคะแนนจากความผิดพลาด
    int score1 = 80, score2 = 80; // คะแนนเริ่มต้น
    int penalty = 2, mistake = 3; // ค่าปรับต่อความผิด 1 ครั้ง และจำนวนความผิด
    score1 = score1 - (penalty * (mistake + 1)); 
    // แบบเต็ม: หักคะแนน = 2 * (3+1) = 8, เหลือ 80 - 8 = 72
    score2 -= (penalty * (mistake + 1)); 
    // แบบย่อ: ทำเหมือนกัน
    printf("7) score1 = %d, score2 = %d\n", score1, score2);

    return 0; // จบการทำงานของโปรแกรม
}
