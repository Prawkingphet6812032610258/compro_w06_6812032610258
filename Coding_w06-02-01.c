
#include <stdio.h> // เรียกใช้ฟังก์ชัน printf

int main() {
    // ประกาศตัวแปรจำนวนเต็ม i, j และ k โดยกำหนดค่าเริ่มต้นให้ i = 1, j = 2
    int i = 1, j = 2, k;

    // แสดงค่าของตัวแปร i, j, k ณ จุดเริ่มต้น (k ยังไม่ได้กำหนดค่าชัดเจน)
    printf("1: i = %d, j = %d, k = (ยังไม่กำหนด)\n", i, j);

    // บรรทัดที่ 2: คำนวณค่า k โดยนำ i + j → k = 1 + 2 = 3
    k = i + j;
    printf("2: i = %d, j = %d, k = %d\n", i, j, k);

    // บรรทัดที่ 3: คำนวณค่าใหม่ให้ i → i = i + (k * j) = 1 + (3 * 2) = 1 + 6 = 7
    i = i + (k * j);
    printf("3: i = %d, j = %d, k = %d\n", i, j, k);

    // บรรทัดที่ 4: กำหนดค่าใหม่ให้ j โดยใช้ i / 2 → j = 7 / 2 = 3 (หารจำนวนเต็ม)
    j = i / 2;
    printf("4: i = %d, j = %d, k = %d\n", i, j, k);

    // บรรทัดที่ 5: กำหนดค่าใหม่ให้ k เป็นเศษจากการหาร i ด้วย 2 → k = 7 % 2 = 1
    k = i % 2;
    printf("5: i = %d, j = %d, k = %d\n", i, j, k);

    // บรรทัดที่ 6: คำนวณค่าใหม่ให้ i → i = (j + k) * 3 = (3 + 1) * 3 = 12
    i = (j + k) * 3;
    printf("6: i = %d, j = %d, k = %d\n", i, j, k);

    return 0; // จบโปรแกรม
}