#include <stdio.h>

#include <stdio.h> // เรียกใช้ไลบรารีมาตรฐานสำหรับการแสดงผล (printf)

int main() {
    // ประกาศตัวแปรจำนวนเต็ม A ถึง G
    int A, B, C, D, E, F, G;

    // ----------------- นิพจน์ที่ 1 -----------------
    // A = -2 + 5 * 2;
    // การคูณมีลำดับสูงกว่าการบวก จึงคูณก่อน: 5 * 2 = 10
    // แล้วจึงบวกกับ -2 → -2 + 10 = 8
    A = -2 + 5 * 2;
    printf("A = %d\n", A); // แสดงผล A = 8

    // ----------------- นิพจน์ที่ 2 -----------------
    // B = 10 / 2 * 3;
    // แบ่งกับคูณมีลำดับเท่ากัน จึงประมวลผลจากซ้ายไปขวา
    // 10 / 2 = 5 → 5 * 3 = 15
    B = 10 / 2 * 3;
    printf("B = %d\n", B); // แสดงผล B = 15

    // ----------------- นิพจน์ที่ 3 -----------------
    // C = 6 / 2 + 3 * (4 % 2);
    // เริ่มจากในวงเล็บก่อน: 4 % 2 = 0 (เศษของ 4 หาร 2)
    // จากนั้น: 6 / 2 = 3, 3 * 0 = 0
    // แล้ว: 3 + 0 = 3
    C = 6 / 2 + 3 * (4 % 2);
    printf("C = %d\n", C); // แสดงผล C = 3

    // ----------------- นิพจน์ที่ 4 -----------------
    // D = (5 + 2) * 15 % 4;
    // วงเล็บก่อน: 5 + 2 = 7
    // คูณ: 7 * 15 = 105
    // จากนั้น mod: 105 % 4 = 1
    D = (5 + 2) * 15 % 4;
    printf("D = %d\n", D); // แสดงผล D = 1

    // ----------------- นิพจน์ที่ 5 -----------------
    // E = 6 + 2 * 2 - 6 / 2;
    // เรียงลำดับ: คูณ → 2 * 2 = 4, แบ่ง → 6 / 2 = 3
    // จากนั้นบวกและลบ: 6 + 4 = 10, 10 - 3 = 7
    E = 6 + 2 * 2 - 6 / 2;
    printf("E = %d\n", E); // แสดงผล E = 7

    // ----------------- นิพจน์ที่ 6 -----------------
    // F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    // เรียงลำดับ:
    // 1) คูณ: 3 * 2 = 6
    // 2) แบ่ง: 8 / 4 = 2
    // 3) mod: 6 % 5 = 1
    // ต่อไปดำเนินการจากซ้ายไปขวา:
    // 5 + 6 = 11 → 11 - 2 = 9 → 9 + 1 = 10
    F = 5 + 3 * 2 - 8 / 4 + (6 % 5);
    printf("F = %d\n", F); // แสดงผล F = 10

    // ----------------- นิพจน์ที่ 7 -----------------
    // G = (4 + 3) * 2 - 10 / (2 + 3);
    // เริ่มจากวงเล็บ:
    // (4 + 3) = 7 → (2 + 3) = 5
    // จากนั้นคูณ: 7 * 2 = 14
    // และแบ่ง: 10 / 5 = 2
    // สุดท้าย: 14 - 2 = 12
    G = (4 + 3) * 2 - 10 / (2 + 3);
    printf("G = %d\n", G); // แสดงผล G = 12

    return 0; // จบการทำงานของโปรแกรม
}