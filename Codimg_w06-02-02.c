
#include <stdio.h> // เรียกใช้ฟังก์ชัน printf

int main() {
    // ประกาศตัวแปรแบบทศนิยม (double) x, y และ z
    double x = 1.0, y = 2.0, z;

    // แสดงค่าของตัวแปรเริ่มต้น
    printf("1: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // บรรทัดที่ 2: กำหนดค่าใหม่ให้ x → x = y + 5.0 = 2.0 + 5.0 = 7.0
    x = y + 5.0;
    printf("2: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // บรรทัดที่ 3: กำหนดค่าใหม่ให้ y → y = x / 2.0 = 7.0 / 2.0 = 3.5
    y = x / 2.0;
    printf("3: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // บรรทัดที่ 4: กำหนดค่าใหม่ให้ y → y = (x * 3.0) + 4.0 = (7.0 * 3.0) + 4.0 = 25.0
    y = (x * 3.0) + 4.0;
    printf("4: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // บรรทัดที่ 5: กำหนดค่าใหม่ให้ x → x = -0.5 - y = -0.5 - 25.0 = -25.5
    x = -0.5 - y;
    printf("5: x = %.1f, y = %.1f, z = (ยังไม่กำหนด)\n", x, y);

    // บรรทัดที่ 6: กำหนดค่าให้ z → z = x + y = -25.5 + 25.0 = -0.5
    z = x + y;
    printf("6: x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0; // จบโปรแกรม
}