#include <stdio.h>

int main() {
    int a = 5, b = 17;
    float c = 8.5, d = 4.0;

    // 1. แสดงค่าที่รับเข้าของตัวแปรทั้งหมด
    printf("a = %d, b = %d, c = %.2f, d = %.2f\n", a, b, c, d);

    // 2. ผลการคำนวณของ a + b
    printf("a + b = %d\n", a + b); // บวกจำนวนเต็ม

    // 3. ผลการคำนวณของ a - b
    printf("a - b = %d\n", a - b); // ลบจำนวนเต็ม

    // 4. ผลการคำนวณของ c * d
    printf("c * d = %.2f\n", c * d); // คูณทศนิยม

    // 5. ผลการคำนวณของ a * c
    printf("a * c = %.2f\n", a * c); // คูณ int กับ float

    // 6. ผลการคำนวณของ c / d
    printf("c / d = %.2f\n", c / d); // หาร float กับ float

    // 7. ผลการคำนวณของ a / b
    printf("a / b = %d\n", a / b); // หารจำนวนเต็ม (int) = 0

    // 8. ผลการคำนวณของ a / c
    printf("a / c = %.2f\n", a / c); // หาร int กับ float

    // 9. ผลการคำนวณของ c % a
    // printf("c %% a = %.2f\n", c % a); // ❌ ERROR: ใช้ % กับ float ไม่ได้
    // เกิด error เพราะเครื่องหมาย % ใช้ได้เฉพาะจำนวนเต็ม (int) เท่านั้น
    // วิธีแก้: แปลง c ให้เป็น int ก่อน
    printf("(int)c %% a = %d\n", ((int)c % a)); // = 8 % 5 = 3

    // 10. ผลการคำนวณของ c % d
    // printf("c %% d = %.2f\n", c % d); // ❌ ERROR: ใช้ % กับ float ไม่ได้
    // แก้ไม่ได้โดยตรง เพราะ c และ d เป็น float ทั้งคู่
    // ทางเลือก: แปลงทั้งคู่เป็น int ก่อน
    printf("(int)c %% (int)d = %d\n", ((int)c % (int)d)); // = 8 % 4 = 0

    return 0;
}
// สรุปอธิบายผลลัพธ์และข้อสังเกต
//1. a + b และ a - b ใช้ได้ปกติ เพราะเป็นการดำเนินการระหว่างจำนวนเต็ม (int)
//2. c * d, a * c, c / d, a / c ใช้ได้ เพราะ float รองรับการคูณและหารระหว่างประเภทต่าง ๆ
//3. a / b เป็นการหารจำนวนเต็ม ผลลัพธ์จะถูกปัดเศษทิ้ง เหลือแค่ 0 (เพราะ 5/17 < 1)
//4. % (modulo) ใช้ได้ เฉพาะกับจำนวนเต็มเท่านั้น → ใช้กับ float จะ error
//5. การแก้ % ที่มี float: ต้องแปลง float เป็น int ก่อน โดยใช้ (int)
